public class Operadores {

    public static void main(String[] args) {

        int a = 2;
        int b = 2;
        int c = a+ b;
        System.out.println(c);

        int aSub = 2;
        int bSub = 1;
        int cSub = aSub - bSub;
        System.out.println(cSub);

        int aMult = 2;
        int bMult = 1;
        int cMult = aMult * bMult;
        System.out.println(cMult);

        int aDiv  = 2;
        int bDiv = 1;
        int cDiv = aDiv / bDiv;
        System.out.println(cDiv);

        // A porcentagem que seria o resto da divisao é atribuido o sinal de %


    }


}
