public class While {
    public static void main(String[] args) {

        int idade = 20;
        System.out.println("Inicio da verificacao: ");

        while (idade <=18){
            System.out.println("Voce nao pode ter habilitacao: ");
            idade++;
        }
        System.out.println("Parabens voce agora pode ter habilitacao: ");

    }
}
