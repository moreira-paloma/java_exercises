public class Foguete {

    public static void main(String[] args) {
        int num = 5;

        System.out.println("Lancamento em alguns segundos ...");

        while (num >= 0) {
            System.out.println("Iniciando em ..." + num);
            num--;

        }

        System.out.println("Foguete lancado! ");
    }
}







