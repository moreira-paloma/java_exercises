public class For {

    public static void main(String[] args) {

        // for controla mais quantas x a operacao vai acontecer
        //verificacao de lista de dados,o for é o mais usado.

        System.out.println("Iniciando verificacao: ");
            //contador,  condicao,   incremento  - mais usado para percorrer listas
        for(int idade =15; idade<=18; idade++){
            System.out.println("Voce nao pode ter habilitacao: ");
        }
        System.out.println("Agora voce pode ter habilitacao: ");
    }
}


